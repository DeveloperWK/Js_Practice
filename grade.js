var mark;

mark=prompt('Enter Your Mark :');

if(mark>=80){
	document.write('<h1>You Got A+ </h1>');
}else if(mark>=75){
	document.write('<h1>You Got A </h1>');
}else if(mark>=70){
	document.write('<h1>You Got A- </h1>');
}else if(mark>=65){
	document.write('<h1>You Got B+ </h1>');
}else if(mark>=60){
	document.write('<h1>You Got B </h1>');
}else if(mark>=55){
	document.write('<h1>You Got B- </h1>');
}else if(mark>=50){
	document.write('<h1>You Got C+ </h1>');
}else if(mark>=45){
	document.write('<h1>h1You Got C <h1>');
}else if(mark>=40){
	document.write('<h1>You Got D </h1>');
}else{
	document.write('<h1>You are Fail </h1>');
};


